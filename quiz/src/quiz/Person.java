package quiz;

public class Person {
	
		String ssn;
		String name;
		String address;
		
		public Person() {
			
		}
	
		public Person(String ssn, String name, String address) {
			this.ssn = ssn;
			this.name = name;
			this.address = address;
		}	
	
		public String getSsn() {
			return getSsn();
		}
		public String getName() {
			return getName();
		}
		public String getAddress() {
			return getAddress();
		}

	}


